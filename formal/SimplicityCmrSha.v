(* BIP340 tagged-hash IV: midstate after H(tag) || H(tag). *)
Definition bip340_iv_of (tag_bytes : list N) : list N :=
  let th := sha256 tag_bytes in
  state_to_bytes (compress_words H0const (bytes_to_words (th ++ th))).

(* "Simplicity" 0x1f "Jet" *)
Definition simplicity_jet_tag : list N :=
  [ 0x53;0x69;0x6d;0x70;0x6c;0x69;0x63;0x69;0x74;0x79; 0x1f; 0x4a;0x65;0x74 ]%N.
Definition JET_IV : list N := bip340_iv_of simplicity_jet_tag.

(* update_with_weight: left block = 24 zero bytes ++ weight as 8 big-endian bytes. *)
Definition cmr_update_with_weight (iv : list N) (weight : nat) (r : list N) : list N :=
  cmr_update iv (repeat 0%N 24 ++ n_to_bytes_be 8 (N.of_nat weight)) r.

Definition bit_cmr (b : bool) : list N := if b then BITS1 else BITS0.

(* Collapse the scribe stack while the running index [j] is odd (rust's
   `while j & 1 == 1` loop): pop right, pop left, push pair(left,right). *)
Fixpoint scribe_collapse (fuel j : nat) (stack : list (list N)) : list (list N) :=
  match fuel with
  | O => stack
  | S f =>
      if Nat.odd j then
        match stack with
        | r :: l :: tl => scribe_collapse f (Nat.div2 j) (scmr_pair l r :: tl)
        | _ => stack
        end
      else stack
  end.

Fixpoint scribe_build (idx : nat) (bits : list bool) (stack : list (list N))
  : list (list N) :=
  match bits with
  | [] => stack
  | b :: rest => scribe_build (S idx) rest (scribe_collapse 32 idx (bit_cmr b :: stack))
  end.

Definition scribe_cmr (value_bits : list bool) : list N :=
  match scribe_build 0 value_bits [] with
  | c :: _ => c
  | [] => UNIT_IV      (* unreachable: words have >= 1 bit *)
  end.

(* Full const_word CMR for the tree's SWord (encoded_width, value_bits):
   n = encoded_width - 1, bit length = 2^n = length value_bits. *)
Definition cmr_word_bytes (encoded_width : nat) (value_bits : list bool) : list N :=
  let n := Nat.pred encoded_width in
  let sc := scribe_cmr value_bits in
  let ihr1 := cmr_update1 CONST_WORD_IV sc in
  let ihr2 := cmr_update ihr1 TMR_UNIT (nth n TWO_TWO_N (repeat 0%N 32)) in
  cmr_update_with_weight JET_IV (List.length value_bits) ihr2.

Lemma cmr_word_bytes_length :
  forall w vb, length (cmr_word_bytes w vb) = 32%nat.
Proof. intros; unfold cmr_word_bytes, cmr_update_with_weight; apply cmr_update_length. Qed.
