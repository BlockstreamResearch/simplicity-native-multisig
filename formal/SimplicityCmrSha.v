(* ---- Compression from a 32-byte midstate IV. ---- *)

(* A 32-byte big-endian midstate as the 8-word chaining state. *)
Definition bytes32_to_state (bs : list N) : state :=
  match bytes_to_words bs with
  | a :: b :: c :: d :: e :: f :: g :: h :: _ => (a, b, c, d, e, f, g, h)
  | _ => H0const
  end.

(* update(iv, left, right): one block compression of (left ++ right). *)
Definition cmr_update (iv l r : list N) : list N :=
  state_to_bytes (compress_words (bytes32_to_state iv) (bytes_to_words (l ++ r))).

(* update_1(iv, child): one block compression of (32 zero bytes ++ child). *)
Definition cmr_update1 (iv c : list N) : list N :=
  state_to_bytes (compress_words (bytes32_to_state iv) (bytes_to_words (repeat 0%N 32 ++ c))).

(* ---- Simplicity CMR combinators (byte-level; match cmr.rs). ---- *)

Definition scmr_iden : list N := IDEN_IV.
Definition scmr_unit : list N := UNIT_IV.
Definition scmr_witness : list N := WITNESS_IV.
Definition scmr_comp (l r : list N) : list N := cmr_update COMP_IV l r.
Definition scmr_case (l r : list N) : list N := cmr_update CASE_IV l r.
Definition scmr_pair (l r : list N) : list N := cmr_update PAIR_IV l r.
Definition scmr_injl (c : list N) : list N := cmr_update1 INJL_IV c.
Definition scmr_injr (c : list N) : list N := cmr_update1 INJR_IV c.
Definition scmr_take (c : list N) : list N := cmr_update1 TAKE_IV c.
Definition scmr_drop (c : list N) : list N := cmr_update1 DROP_IV c.
Definition scmr_disconnect (c : list N) : list N := cmr_update1 DISCONNECT_IV c.
Definition scmr_assertl (l r : list N) : list N := cmr_update CASE_IV l r.
Definition scmr_assertr (l r : list N) : list N := cmr_update CASE_IV l r.
Definition scmr_fail (l r : list N) : list N := cmr_update FAIL_IV l r.

(* ---- Every CMR is 32 bytes. ---- *)

Lemma cmr_update_length : forall iv l r, length (cmr_update iv l r) = 32%nat.
Proof. intros; unfold cmr_update; apply state_to_bytes_length. Qed.

Lemma cmr_update1_length : forall iv c, length (cmr_update1 iv c) = 32%nat.
Proof. intros; unfold cmr_update1; apply state_to_bytes_length. Qed.

(* ---- Self-contained faithfulness check (the Step-3 trust anchor). ----
   rust-simplicity states BITS[0] = injl(unit), BITS[1] = injr(unit).  Recomputing
   them through our SHA-256 + midstate layer and matching the pinned constants
   validates the compression, the big-endian midstate byte order, the zeros-first
   update_1 block layout, and the copied IVs all at once. *)

Example injl_unit_matches_bits0 : scmr_injl scmr_unit = BITS0.
Proof. vm_compute. reflexivity. Qed.

Example injr_unit_matches_bits1 : scmr_injr scmr_unit = BITS1.
Proof. vm_compute. reflexivity. Qed.

