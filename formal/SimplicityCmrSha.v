(*
  SimplicityCmrSha.v — the Simplicity commitment-Merkle-root (CMR) construction
  built on the self-contained SHA-256 of Sha256Core.v.  This is Step 3 of the G5
  Route B plan: reproduce, byte-for-byte, the CMR combinators that rust-simplicity
  (the deployed compiler) uses, so the byte decoder can compute the REAL CMR of a
  decoded program with no exporter/foundation trust.

  Ground truth: rust-simplicity/src/merkle/cmr.rs.  There every combinator CMR is
  a SHA-256 midstate obtained by continuing SHA-256 from a per-combinator tag IV:

    - nullary (iden/unit/witness)      : the IV itself
    - unary  (injl/injr/take/drop/...) : update_1(IV, child)
        = one block compression of  (32 zero bytes ++ child)   [zeros FIRST]
    - binary (comp/case/pair)          : update(IV, left, right)
        = one block compression of  (left ++ right)
    - assertl/assertr                  : share the case tag (update(CASE_IV,l,r))

  The IV byte arrays below are copied verbatim from cmr.rs.  Faithfulness of the
  whole construction is checked below WITHOUT any external tool: rust-simplicity
  documents that BITS[0] = injl(unit) and BITS[1] = injr(unit); we recompute both
  by vm_compute and check they equal the pinned BITS constants.  A mismatch would
  reveal a byte-order or block-layout error before we ever run the full program.
*)

